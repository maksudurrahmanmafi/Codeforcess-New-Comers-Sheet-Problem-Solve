#include <bits/stdc++.h>
using namespace std;
#define ll long long int

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int n,m;
    cin>>n>>m;
    cout<<n<<" + "<<m<<" = "<<n+m<<'\n';
    cout<<n<<" * "<<m<<" = "<<((long long)n*m)<<'\n';
    cout<<n<<" - "<<m<<" = "<<n-m<<'\n';
    return 0;
}
